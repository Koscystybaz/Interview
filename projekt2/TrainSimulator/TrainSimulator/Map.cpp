Map::Map()
{
	grass = TextureManager::LoadTexture("assets/grass.png");
	rail = TextureManager::LoadTexture("assets/rail.png");
	rail2 = TextureManager::LoadTexture("assets/rail2.png");
	rail3 = TextureManager::LoadTexture("assets/rail3.png");
	rail4 = TextureManager::LoadTexture("assets/rail4.png");
	rail5 = TextureManager::LoadTexture("assets/rail5.png");
	rail6 = TextureManager::LoadTexture("assets/rail6.png");

	LoadMap(map1);

	src.x = 0;
	src.y = 0;
	src.h = 32;
	src.w = 32;

	dest.x = 0;
	dest.y = 0;
	dest.h = 32;
	dest.w = 32;

}

Map::~Map()
{	

}

void Map::LoadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = map[row][column];

			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(grass, src, dest);
				break;
			case 1:
				TextureManager::Draw(rail, src, dest);
				break;
			case 2:
				TextureManager::Draw(rail2, src, dest);
				break;
			case 3:
				TextureManager::Draw(rail3, src, dest);
				break;
			case 4:
				TextureManager::Draw(rail4, src, dest);
				break;
			case 5:
				TextureManager::Draw(rail5, src, dest);
				break;
			case 6:
				TextureManager::Draw(rail6, src, dest);
				break;


			default:
				break;
			}
		}
	}
}